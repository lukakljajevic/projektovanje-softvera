    private void btnDodajPredmetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajPredmetActionPerformed
        int row = tabelaPronadjeniPredmeti.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Morate izabrati predmet!");
        } else {
            Predmet predmet = listaPredmetaPretraga.get(row);
            String brojCasovaString = txtBrojCasova.getText();
            int brojCasova = 0;
            try {
                brojCasova = Integer.parseInt(brojCasovaString);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Broj casova mora biti broj!");
                return;
            }
            if (brojCasova < 0) {
                JOptionPane.showMessageDialog(this, "Broj casova mora biti veci od 0!");
                return;
            }

            if (!sadrziPredmet(predmet)) {
                nastavnik.getListaPredavanja().add(new Predaje(nastavnik, predmet, brojCasova));
            } else {
                JOptionPane.showMessageDialog(this, "Predmet je vec dodat!");
            }

            osveziTabele();
        }
    }//GEN-LAST:event_btnDodajPredmetActionPerformed


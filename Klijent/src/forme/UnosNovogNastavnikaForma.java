    private void btnDodajUsavrsavanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajUsavrsavanjeActionPerformed
        String naziv = txtNazivUsavrsavanja.getText();
        String institucija = txtNazivInstitucijeUsavrsavanja.getText();
        String godinaString = txtGodinaUsavrsavanja.getText();
        int godina = 0;
        try {
            godina = Integer.parseInt(godinaString);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Godina mora biti broj!");
            return;
        }

        
        
        if (godina < 0) {
            JOptionPane.showMessageDialog(this, "Godina mora biti veca od 0!");
            return;
        }
        if (naziv.equals("") || institucija.equals("")) {
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
        } else {
            nastavnik.getListaUsavrsavanja().add(new Usavrsavanje(0, naziv, institucija, godina, nastavnik));
            osveziTabele();

        }

    }//GEN-LAST:event_btnDodajUsavrsavanjeActionPerformed


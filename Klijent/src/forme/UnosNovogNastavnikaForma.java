    public void podesiFormu() {
        comboTipNastavnika.removeAllItems();
        comboTipUspeha.removeAllItems();

        for (TipNastavnika tipNastavnika : listaTipovaNastavnika) {
            comboTipNastavnika.addItem(tipNastavnika);
        }

        for (TipUspeha tipUspeha : listaTipovaUspeha) {
            comboTipUspeha.addItem(tipUspeha);
        }
    }

    public void osveziTabele() {
        ModelTabeleAkademskiUspeh modelTabeleAkademskiUspeh = (ModelTabeleAkademskiUspeh) tabelaAkademskiUspesi.getModel();
        modelTabeleAkademskiUspeh.fireTableDataChanged();

        ModelTabeleUsavrsavanja modelTabeleUsavrsavanja = (ModelTabeleUsavrsavanja) tabelaUsavrsavanja.getModel();
        modelTabeleUsavrsavanja.fireTableDataChanged();

        ModelTabeleReference modelTabeleReferencePretraga = (ModelTabeleReference) tabelaPronadjeneReference.getModel();
        modelTabeleReferencePretraga.fireTableDataChanged();

        ModelTabeleReference modelTabeleReferenceNastavnik = (ModelTabeleReference) tabelaDodateReference.getModel();
        modelTabeleReferenceNastavnik.fireTableDataChanged();

        ModelTabelePretragaPredmet modelTabelePretragaPredmetPretraga = (ModelTabelePretragaPredmet) tabelaPronadjeniPredmeti.getModel();
        modelTabelePretragaPredmetPretraga.fireTableDataChanged();

        ModelTabelePredaje modelTabelePredaje = (ModelTabelePredaje) tabelaDodatiPredmeti.getModel();
        modelTabelePredaje.fireTableDataChanged();

    }

    private boolean sadrziPredmet(Predmet predmet) {
        for (Predaje predaje : nastavnik.getListaPredavanja()) {
            if (predaje.getPredmet().getPredmetID() == predmet.getPredmetID()) {
                return true;
            }
        }
        return false;
    }

    private boolean sadrziReferencu(Referenca referenca) {
        for (Referenca r : nastavnik.getListaReferenci()) {
            if (r.getReferencaID() == referenca.getReferencaID()) {
                return true;
            }
        }
        return false;
    }

    public void uspesnoUnetNastavnik(boolean uspesnoUnetNastavnik) {
        if (uspesnoUnetNastavnik) {
            JOptionPane.showMessageDialog(this, "Uspesno unet nastavnik u bazu.");
        } else {
            JOptionPane.showMessageDialog(this, "Greska prilikom unosa nastavnika u bazu.");
        }
    }


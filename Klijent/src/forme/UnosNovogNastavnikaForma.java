    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        String imePrezime = txtImePrezime.getText();
        String institucija = txtInstitucija.getText();
        String naucnaOblast = txtNaucnaOblast.getText();
        TipNastavnika tipNastavnika = (TipNastavnika) comboTipNastavnika.getSelectedItem();

        if (!imePrezime.equals("") || !institucija.equals("") || !naucnaOblast.equals("")) {
            nastavnik.setImePrezime(imePrezime);
            nastavnik.setInstitucija(institucija);
            nastavnik.setNaucnaOblast(naucnaOblast);
            nastavnik.setTipNastavnika(tipNastavnika);
            
            KontrolerKlijent.getInstance().unesiNastavnika(nastavnik);
            
        } else {
            JOptionPane.showMessageDialog(this, "Svi licni podaci o nastavniku moraju biti uneti!");
        }


    }//GEN-LAST:event_btnSacuvajActionPerformed


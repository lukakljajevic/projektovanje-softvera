    private void btnDodajAutoraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajAutoraActionPerformed
        int row = tabelaPretragaAutora.getSelectedRow();
        
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Morate izabrati autora!");
        } else {
            SpoljniAutor sa = listaAutoraPretraga.get(row);
            if (!sadrziAutora(sa)) {
                referenca.getListaSpoljnihAutora().add(sa);
                osveziTabele();
            } else {
                JOptionPane.showMessageDialog(this, "Autor je vec dodat!");
            }
        }
    }//GEN-LAST:event_btnDodajAutoraActionPerformed


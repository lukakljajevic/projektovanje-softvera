    private boolean sadrzinastavnika(Nastavnik n) {
        for (Nastavnik nastavnik : referenca.getListaNastavnika()) {
            if (nastavnik.getNastavnikID() == n.getNastavnikID()) {
                return true;
            }
        }
        return false;
    }

    private boolean sadrziAutora(SpoljniAutor sa) {
        for (SpoljniAutor spa : referenca.getListaSpoljnihAutora()) {
            if (spa.getAutorID() == sa.getAutorID()) {
                return true;
            }
        }
        return false;
    }

    public void uspesnoUnetaReferenca(boolean uspesnoUnetaReferenca) {
        if (uspesnoUnetaReferenca) {
            JOptionPane.showMessageDialog(this, "Uspesno uneta referenca u bazu!");
        } else {
            JOptionPane.showMessageDialog(this, "Greska prilikom unosa reference u bazu!");
        }
    }

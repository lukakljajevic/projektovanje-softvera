    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        String naslov = txtNaslov.getText();
        String godinaString = txtGodina.getText();
        int godina = 0;
        try {
            godina = Integer.parseInt(godinaString);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Godina mora biti broj!");
            return;
        }
        
        referenca.setNaslov(naslov);
        referenca.setGodina(godina);
        KontrolerKlijent.getInstance().unesiReferencu(referenca);
    }//GEN-LAST:event_btnSacuvajActionPerformed


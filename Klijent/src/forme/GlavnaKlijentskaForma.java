    private void srediTabele() {
        ModelTabelePretragaNastavnik mtp = new ModelTabelePretragaNastavnik();
        mtp.setForma("glavnaKlijentForma");
        tabelaNastavnikPretraga.setModel(mtp);
        ModelTabelePretragaPredmet mtpp = new ModelTabelePretragaPredmet();
        tabelaPredmetiPretraga.setModel(mtpp);
    }

    private void srediComboBox() {
        comboVrstaPretrage.removeAllItems();
        comboVrstaPretrage.addItem("Nastavnik");
        comboVrstaPretrage.addItem("Predmet");
    }


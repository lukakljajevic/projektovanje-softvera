    private void comboVrstaPretrageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboVrstaPretrageActionPerformed
        String kategorija = (String) comboVrstaPretrage.getSelectedItem();
        if (kategorija == "Nastavnik") {
            panelNastavnici.setVisible(true);
            panelPredmeti.setVisible(false);
        } else {
            panelNastavnici.setVisible(false);
            panelPredmeti.setVisible(true);
        }
    }//GEN-LAST:event_comboVrstaPretrageActionPerformed

